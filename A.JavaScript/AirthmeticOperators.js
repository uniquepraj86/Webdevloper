let x=40;
let y=20;

// addition
console.log('x+y =',x+y);

// Substraction
console.log('x-y =',x-y);

//Multiplication
console.log('x*y=',x*y);

//Division
console.log('x/y=',x/y);

//Reminder
console.log('x % y = ', x % y); 

//Increment
console.log('++x =',++x);
console.log('x++ =',x++);
console.log('x =',x);

// decrement
console.log('--x = ', --x); 
console.log('x-- = ', x--); 
console.log('x = ', x);     

//exponentiation
console.log('x ** y =', x ** y);




 






